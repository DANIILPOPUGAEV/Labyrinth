namespace Labyrinths.Enums;

/// <summary>
/// Определяет типы клеток (ячеек) в лабиринте.
/// Используется для представления различных состояний клеток сетки лабиринта.
/// </summary>
public enum CellType
{
    /// <summary>
    /// Стена - непроходимое препятствие.
    /// Отображается как '#' (ASCII) или '█' (Unicode).
    /// </summary>
    Wall,

    /// <summary>
    /// Пустая клетка - проходимое пространство.
    /// Отображается как ' ' (пробел).
    /// </summary>
    Empty,

    /// <summary>
    /// Стартовая точка - начало маршрута.
    /// Отображается как 'O' (ASCII) или '▲' (Unicode).
    /// </summary>
    Start,

    /// <summary>
    /// Конечная точка - цель маршрута.
    /// Отображается как 'X' (ASCII) или '▼' (Unicode).
    /// </summary>
    End,

    /// <summary>
    /// Путь - часть найденного маршрута между стартом и финишем.
    /// Отображается как '.' (ASCII) или '•' (Unicode).
    /// </summary>
    Path
}